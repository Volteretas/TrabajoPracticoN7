    private void jbGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbGuardarActionPerformed
        // TODO add your handling code here:
        boolean iguales = false;
        Menu menu = new Menu();
        Materia a = new Materia(Integer.parseInt(jtCodigo.getText()), jtNombre.getText(),Integer.parseInt(jtAnio.getText()));
        
        for(int i=0; i < materias.size(); i++){
            if(materias.get(i).getIdMateria()== a.getIdMateria()){
                iguales = true;
            }
        }
        
        if(iguales){
            JOptionPane.showMessageDialog(menu, "La materia que intenta crear ya existe");
        }else{
            materias.add(a);
            JOptionPane.showMessageDialog(menu, "Se creo la materia correctamente");
        }

    }//GEN-LAST:event_jbGuardarActionPerformed

