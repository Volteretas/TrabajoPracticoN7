    private void activarBoton(){
        if(jtLegajo.getText().isEmpty() || jtApellido.getText().isEmpty() || jtNombre.getText().isEmpty()){
            jbGuardar.setEnabled(false);
        }else{
            jbGuardar.setEnabled(true);
        }
    }
    
