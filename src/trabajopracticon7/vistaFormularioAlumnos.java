    private void jbGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbGuardarActionPerformed
        // TODO add your handling code here:
        boolean iguales = false;
        Menu menu = new Menu();
        Alumno a = new Alumno(Integer.parseInt(jtLegajo.getText()), jtNombre.getText(), jtApellido.getText());
        
        for(int i=0; i < alumnos.size(); i++){
            if(alumnos.get(i).getLegajo() == a.getLegajo()){
                iguales = true;
            }
        }
        
        if(iguales){
            JOptionPane.showMessageDialog(menu, "El alumno que intenta crear ya existe");
        }else{
            alumnos.add(a);
            JOptionPane.showMessageDialog(menu, "Se creo el alumno correctamente");
        }
        
    }//GEN-LAST:event_jbGuardarActionPerformed

